public class OperasiPerbandingan {
    public static void main(String[] args) {
        //operator perbandingan            ket
        //      >                       lebih dari
        //      <                       kurang dari
        //      >=                      lebih dari samadengan
        //      <=                      kurang dari samadengan
        //      =                       samadengan
        //      !=                      tidak samadengan

        int x = 50;
        int y = 50;

        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x == y);
        System.out.println(x != y);
    }
}
