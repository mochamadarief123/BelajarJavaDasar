public class TipeDataString {
    public static void main(String[] args) {
        //tipe data String adalah tipe data yg menampung lebih dari satu character
        // menggunakan tanda petik dua

        String firsName = "Mochamad Arief";
        String lastName = "Nurhidayat";

        System.out.println(firsName);
        System.out.println(lastName);

        //menggabungkan dua atau lebih string bisa menggunakan keyword +
        String fullName = firsName + " " + lastName;
        System.out.println(fullName);
    }
}
